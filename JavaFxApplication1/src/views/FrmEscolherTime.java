    private void OkJogadorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_OkJogadorActionPerformed
        // TODO add your handling code here:
        
        for(Equipe equipe : equipes){
           if(equipe.getNome().equals(cmbEscolherTime.getSelectedItem())){
               mandante = equipe;
           }
        }  
        
        equipes.remove(mandante);
        cmbEscolherTime.setEnabled(false);
        OkJogador.setEnabled(false);

        if(OkAdversario.isEnabled()){
             cmbEscolherAdv.removeAllItems();

             for(Equipe equipe : equipes){
                 cmbEscolherAdv.addItem(equipe.getNome());
             }
        }else{
            btnSeguir.setEnabled(true);
        }
        
        
    }//GEN-LAST:event_OkJogadorActionPerformed

    private void OkAdversarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_OkAdversarioActionPerformed
        // TODO add your handling code here:
        for(Equipe equipe : equipes){
           if(equipe.getNome().equals(cmbEscolherAdv.getSelectedItem())){
               visitante = equipe;
           }
        }
        
        equipes.remove(visitante);
        cmbEscolherAdv.setEnabled(false);
        OkAdversario.setEnabled(false);

        if(OkJogador.isEnabled()){
             cmbEscolherTime.removeAllItems();

             for(Equipe equipe : equipes){
                 cmbEscolherTime.addItem(equipe.getNome());
             }
        }else{
            btnSeguir.setEnabled(true);
        }
        
        
    }//GEN-LAST:event_OkAdversarioActionPerformed

