    private void cmbEscolherTimeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbEscolherTimeActionPerformed
        // TODO add your handling code here:
       for(Equipe equipe : equipes){
           if(equipe.getNome().equals(cmbEscolherTime.getSelectedItem())){
               lblJogador1.setText(equipe.getJogadores().get(0).getNome());
               prgJogador1.setValue(equipe.getJogadores().get(0).getOverall());
               lblJogador2.setText(equipe.getJogadores().get(1).getNome());
               prgJogador2.setValue(equipe.getJogadores().get(1).getOverall());
               lblJogador3.setText(equipe.getJogadores().get(2).getNome());
               prgJogador3.setValue(equipe.getJogadores().get(2).getOverall());
               prgEntrosamento1.setValue((int) equipe.getEntrosamento());
           }
       }
       
       
        
    }//GEN-LAST:event_cmbEscolherTimeActionPerformed

